//Adding Operator

let x = 10;
let y = 12;
let sum = x + y;

console.log("x:", x, "y:", y, "sum:", sum);


//Substraction Operator

let a = 50;
let b = 60;
let sub;

if (a > b) {
    sub = a - b;
}
else {
    sub = b - a;
}

console.log("a:", a, "b:", b, "substruction:", sub);


